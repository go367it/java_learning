public class RelationalOperators {
    public static void main(String[] args){
        // relational operators
        int a = 10;
        int b = 10;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
